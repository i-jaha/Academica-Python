def km_to_miles(km):
    km = float(km)
    ml = km * 0.62137
    return ml

def celsius_to_fahrenheit(celsius):
    celsius = float(celsius)
    fahrenheit = (celsius * 9 / 5) + 32
    return fahrenheit

def liters_to_gallons(liters):
    liters = float(liters)
    gallons = liters * 0.264172
    return gallons
